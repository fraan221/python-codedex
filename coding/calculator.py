def add(a, b):
    result = a + b
    return a + b

def subtract(a, b):
    result = a - b
    return result

def multiply(a, b):
    result = a * b
    return result

def divide(a, b):
    result = a / b
    return result

def exp(a, b):
    result = a ** b
    return result

